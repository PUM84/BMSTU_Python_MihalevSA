def task2():
    print("\n=== Задача 2. Настольный теннис ===")
    A = int(input("Стоимость комплекта ракеток (руб): "))
    B = int(input("Стоимость одного шарика (руб): "))
    C = int(input("Сколько всего денег у Феофилакта (руб): "))
    balls = (C - A) // B
    print(f"Феофилакт купит {balls} шариков")

def task3():
    print("\n=== Задача 3. Предпоследняя цифра ===")
    n = int(input("Введите натуральное число: "))
